package yoly.com.android.yoly.ui.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.LinkedList;

public class BeforeLoginPagerAdapter extends FragmentPagerAdapter {
    private LinkedList<Fragment> fragments;

    public BeforeLoginPagerAdapter(FragmentManager fm, LinkedList<Fragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public int getCount() {
        return fragments.size();
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }
}